public class Comparar {
    
    public static String MayorMenor (double A, double B){
        
        if (A>B){
            return "A es mayor que B";
        }
        else if (A<B){
            return "A es menor que B";
        }
        else{
            return "A es igual que B";
        }
      
    }
    
}
